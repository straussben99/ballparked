export function getStadiumById(id: string): Stadium | undefined {
  return STADIUMS.find((stadium) => stadium.id === id);
}

export function getStadiumsByDivision(division: Division): Stadium[] {
  return STADIUMS.filter((stadium) => stadium.division === division);
}

export function getStadiumsByLeague(league: League): Stadium[] {
  return STADIUMS.filter((stadium) => stadium.league === league);
}

export function searchStadiums(query: string): Stadium[] {
  const lowerQuery = query.toLowerCase();
  return STADIUMS.filter(
    (stadium) =>
      stadium.name.toLowerCase().includes(lowerQuery) ||
      stadium.team.toLowerCase().includes(lowerQuery) ||
      stadium.city.toLowerCase().includes(lowerQuery) ||
      stadium.state.toLowerCase().includes(lowerQuery) ||
      stadium.teamAbbr.toLowerCase().includes(lowerQuery)
  );
}

export function getAllDivisions(): Division[] {
  return ['AL East', 'AL Central', 'AL West', 'NL East', 'NL Central', 'NL West'];
}
